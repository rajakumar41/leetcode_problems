class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        long long res =arr[0];
        long long cur = res;
        for(int i=1;i<n;i++){
            cur = (cur <0) ? arr[i] : cur + arr[i];
            
            res = max(cur,res);
        }
        return res;
    }
};

